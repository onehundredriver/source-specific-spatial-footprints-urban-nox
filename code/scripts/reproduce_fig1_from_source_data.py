def draw_box(ax, xy, w, h, text, fc="#F5F5F5", ec="0.55", fontsize=FS_SMALL, bold=False) -> None:
    x, y = xy
    rect = Rectangle((x, y), w, h, facecolor=fc, edgecolor=ec, lw=1.0)
    ax.add_patch(rect)
    ax.text(x + w / 2, y + h / 2, text, ha="center", va="center",
            fontsize=fontsize, fontweight=("bold" if bold else "normal"))

