# Panel C is intentionally drawn as TWO clear sub-figures:
#   left  = initial upwind 3 × 7 sector-grid: three 60° sectors × seven distance bands
#   right = reduced 2 × 7 representation: centre upwind sector + aggregated side sector
# The two sub-figures are placed in inset axes so that labels, arrows and the
# distance-band legend do not overlap the sector geometry.

def _setup_sector_inset(ax) -> None:
    hide_ax(ax)
    ax.set_xlim(-1.08, 1.08)
    ax.set_ylim(-0.23, 1.08)
    ax.set_aspect("equal", adjustable="box")


def _draw_sector_grid_core(
    ax,
    *,
    final: bool = False,
    show_wind_arrow: bool = False,
    receptor_label: bool = True,
) -> None:
    """Draw an upper-half wind-sensitive sector grid inside an inset axis."""
    _setup_sector_inset(ax)

    # Seven distance bands; the first small radius is kept for the receptor mask.
    radii = np.array([0.09, 0.17, 0.27, 0.39, 0.53, 0.67, 0.82, 0.96])

    # Main filled cells. The two lateral 60° sectors use the same colour in the
    # final representation to indicate aggregation into one side-sector group.
    for r0, r1 in zip(radii[:-1], radii[1:]):
        if final:
            lateral_alpha = 0.52
            centre_alpha = 0.74
            cells = [
                (120, 180, COL_SIDE, lateral_alpha),
                (60, 120, COL_CENTRE, centre_alpha),
                (0, 60, COL_SIDE, lateral_alpha),
            ]
        else:
            left_alpha = 0.50
            centre_alpha = 0.74
            right_alpha = 0.50
            cells = [
                (120, 180, COL_LEFT, left_alpha),
                (60, 120, COL_CENTRE, centre_alpha),
                (0, 60, COL_RIGHT, right_alpha),
            ]
        for theta1, theta2, colour, alpha in cells:
            ax.add_patch(Wedge(
                (0, 0), r1, theta1, theta2, width=r1-r0,
                facecolor=colour, edgecolor="white", lw=0.85,
                alpha=alpha, zorder=2
            ))

    # External grid outlines and radial separators, kept subtle for Nature-style clarity.
    for r in radii[1:]:
        ax.add_patch(Arc((0, 0), width=2*r, height=2*r, theta1=0, theta2=180,
                         color="white", lw=0.60, alpha=0.82, zorder=3))
        ax.add_patch(Arc((0, 0), width=2*r, height=2*r, theta1=0, theta2=180,
                         color=COL_GRID, lw=0.32, alpha=0.70, zorder=4))

    for angle in [0, 60, 120, 180]:
        x, y = pol2cart(radii[-1], angle)
        ax.plot([0, x], [0, y], color="white", lw=0.85, alpha=0.75, zorder=4)

    # Baseline and receptor.
    ax.plot([-radii[-1], radii[-1]], [0, 0], color=COL_GRID, lw=0.75, zorder=5)
    ax.add_patch(Circle((0, 0), 0.055, facecolor="white", edgecolor="black", lw=1.0, zorder=8))
    if receptor_label:
        ax.text(0, -0.062, "Receptor", fontsize=FS_TINY, ha="center", va="top", zorder=9)

    # Text labels are placed well inside sectors and outlined to preserve readability.
    stroke = [pe.withStroke(linewidth=2.6, foreground="white")]
    if final:
        ax.text(0, 0.56, "Centre\nupwind\nsector",
                fontsize=FS_TINY + 0.6, fontweight="bold", ha="center", va="center",
                path_effects=stroke, zorder=10)
        ax.text(-0.55, 0.36, "Merged\nside\nsector",
                fontsize=FS_TINY + 0.4, fontweight="bold", ha="center", va="center",
                path_effects=stroke, zorder=10)
        ax.text(0.55, 0.36, "Merged\nside\nsector",
                fontsize=FS_TINY + 0.4, fontweight="bold", ha="center", va="center",
                path_effects=stroke, zorder=10)
    else:
        ax.text(0, 0.56, "Centre sector\n(60° × 7)",
                fontsize=FS_TINY + 0.5, fontweight="bold", ha="center", va="center",
                path_effects=stroke, zorder=10)
        ax.text(-0.56, 0.36, "Left sector\n(60° × 7)",
                fontsize=FS_TINY + 0.25, fontweight="bold", ha="center", va="center",
                path_effects=stroke, zorder=10)
        ax.text(0.56, 0.36, "Right sector\n(60° × 7)",
                fontsize=FS_TINY + 0.25, fontweight="bold", ha="center", va="center",
                path_effects=stroke, zorder=10)

    if show_wind_arrow:
        ax.add_patch(FancyArrowPatch((0, -0.220), (0, -0.100),
                                     arrowstyle="-|>", mutation_scale=13,
                                     lw=1.25, color="black", zorder=10))
        ax.text(0.14, -0.195, "Wind direction", fontsize=FS_TINY,
                ha="left", va="center", zorder=10)


