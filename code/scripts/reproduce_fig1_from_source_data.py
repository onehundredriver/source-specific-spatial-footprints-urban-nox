def hide_ax(ax) -> None:
    ax.set_xticks([])
    ax.set_yticks([])
    for s in ax.spines.values():
        s.set_visible(False)

def add_panel_label(ax, letter: str, x: float = 0.00, y: float = 1.00, size: int = FS_PANEL) -> None:
    ax.text(
        x, y, letter,
        transform=ax.transAxes,
        fontsize=size,
        fontweight="bold",
        ha="left",
        va="bottom",
        color="black",
        clip_on=False
    )

def pol2cart(r: float, theta_deg: float) -> Tuple[float, float]:
    theta = math.radians(theta_deg)
    return r * math.cos(theta), r * math.sin(theta)

def draw_receptor(ax, xy=(0.50, 0.20), r=0.030, label=True, fontsize=FS_TINY) -> None:
    ax.add_patch(Circle(xy, r, facecolor="white", edgecolor="black", lw=1.1, zorder=5))
    if label:
        ax.text(xy[0], xy[1] - 0.060, "Receptor", ha="center", va="top", fontsize=fontsize)

def draw_car_icon(ax, xy=(0.2, 0.8), scale=1.0, color=COL_VEH) -> None:
    x, y = xy
    w, h = 0.060 * scale, 0.026 * scale
    ax.add_patch(Rectangle((x - w / 2, y - h / 2), w, h, facecolor=color, edgecolor="none", alpha=0.96))
    ax.add_patch(Rectangle((x - w * 0.22, y + h * 0.02), w * 0.44, h * 0.50,
                           facecolor=color, edgecolor="none", alpha=0.96))
    for dx in (-w * 0.28, w * 0.28):
        ax.add_patch(Circle((x + dx, y - h * 0.58), h * 0.25, facecolor="black", edgecolor="none"))

def draw_ship_icon(ax, xy=(0.2, 0.8), scale=1.0, color=COL_SHIP) -> None:
    x, y = xy
    hull = Polygon(
        [(x - 0.038 * scale, y - 0.011 * scale),
         (x + 0.032 * scale, y - 0.011 * scale),
         (x + 0.018 * scale, y + 0.006 * scale),
         (x - 0.046 * scale, y + 0.006 * scale)],
        closed=True, facecolor=color, edgecolor="none", alpha=0.96
    )
    cabin = Rectangle((x - 0.010 * scale, y + 0.006 * scale), 0.020 * scale, 0.015 * scale,
                      facecolor=color, edgecolor="none", alpha=0.96)
    ax.add_patch(hull)
    ax.add_patch(cabin)

def draw_factory_icon(ax, xy=(0.2, 0.8), scale=1.0, color=COL_IND) -> None:
    x, y = xy
