def draw_single_fixed_buffer(ax, center=(0.25, 0.47), radius=0.165) -> None:
    cx, cy = center
    ax.add_patch(Circle((cx, cy), radius, facecolor="none", edgecolor=COL_BUFFER, lw=1.6))
    draw_receptor(ax, (cx, cy), r=0.026, label=True, fontsize=FS_TINY)

    # Dashed radius axis with a question mark, indicating uncertain buffer radius.
    radius_angle = 315  # degree; 0 = right, 90 = up

    x0, y0 = pol2cart(radius * 0.18, radius_angle)
    x1, y1 = pol2cart(radius * 0.98, radius_angle)

    ax.plot(
        [cx + x0, cx + x1],
        [cy + y0, cy + y1],
        linestyle=(0, (3, 2)),
        color="0.35",
        lw=1.2,
        zorder=6
    )

    xq, yq = pol2cart(radius * 0.64, radius_angle)
    ax.text(
        cx + xq, cy + yq, "?",
        fontsize=18,
        fontweight="bold",
        ha="center",
        va="center",
        color="0.25",
        zorder=7
    )

    # multiple source icons inside / near the same ambiguous buffer
    draw_car_icon(ax, (cx - 0.010, cy + 0.055), scale=0.78)
    draw_ship_icon(ax, (cx + 0.120, cy + 0.105), scale=0.78)
    draw_factory_icon(ax, (cx - 0.125, cy + 0.110), scale=0.78)

