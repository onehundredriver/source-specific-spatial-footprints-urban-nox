def load_monitoring_sites(monitoring_sites_file: str):
    """Load compact public station metadata for panel a."""
    if not GEOPANDAS_AVAILABLE:
        raise ImportError("geopandas is required to draw panel a directly.")

    if not os.path.exists(monitoring_sites_file):
        raise FileNotFoundError(f"MONITORING_SITES_FILE does not exist: {monitoring_sites_file}")

    df = pd.read_csv(monitoring_sites_file, encoding="utf-8-sig")
    required = ["Station", "Latitude", "Longitude", "StationType"]
    missing = [c for c in required if c not in df.columns]
    if missing:
        raise ValueError(f"monitoring_sites.csv missing required columns: {missing}")

    df["StationType"] = df["StationType"].fillna("Unknown")

    return gpd.GeoDataFrame(
        df,
        geometry=gpd.points_from_xy(df["Longitude"], df["Latitude"]),
        crs="EPSG:4326"
    ).to_crs(epsg=3857)


def load_traffic_network(traffic_file: str):
    if not GEOPANDAS_AVAILABLE:
        return None
    if not os.path.exists(traffic_file):
        print(f"WARNING: TRAFFIC_FILE does not exist: {traffic_file}")
        return None

    df = pd.read_csv(traffic_file, encoding="utf-8-sig")
    required = ["longitude_start", "latitude_start", "longitude_end", "latitude_end", "avg_total_volume_bpr"]
    missing = [c for c in required if c not in df.columns]
    if missing:
        print(f"WARNING: traffic file missing required columns: {missing}")
        return None

    geometry = [
        LineString([(row.longitude_start, row.latitude_start), (row.longitude_end, row.latitude_end)])
        for row in df.itertuples()
    ]
    gdf = gpd.GeoDataFrame(df, geometry=geometry, crs="EPSG:4326").to_crs(epsg=3857)

    max_val = gdf["avg_total_volume_bpr"].max()
    if pd.isna(max_val) or max_val <= 0:
        gdf["lw"] = 0.30
    else:
        gdf["lw"] = (
            np.log10(gdf["avg_total_volume_bpr"].clip(lower=0) + 1)
            / np.log10(max_val + 1)
        ) * 1.85 + 0.08

    return gdf

def add_basemap(ax) -> None:
    if not USE_BASEMAP:
        return
    try:
        import contextily as ctx
        for attempt in range(2):
            try:
                ctx.add_basemap(ax, source=ctx.providers.CartoDB.Positron,
                                alpha=BASEMAP_ALPHA, zorder=0)
                return
            except Exception as exc:
                if attempt == 1:
                    print(f"WARNING: basemap skipped: {exc}")
                else:
                    time.sleep(1.5)
    except Exception as exc:
        print(f"WARNING: contextily not available; basemap skipped: {exc}")

def set_map_extent_with_reserved_bottom(ax, sites) -> None:
    xmin, ymin, xmax, ymax = sites.total_bounds
    xrange = xmax - xmin
    yrange = ymax - ymin
    ax.set_xlim(xmin - xrange * ZOOM_MARGIN_X, xmax + xrange * ZOOM_MARGIN_X)
    ax.set_ylim(ymin - yrange * ZOOM_MARGIN_BOTTOM, ymax + yrange * ZOOM_MARGIN_TOP)

